namespace frontend\widgets;
use yii\base\Widget;

class FeaturedSectionWidget extends Widget
{
    public $posts_model;
    public $count = 5;

    public function run()
    {
        parent::run(); // TODO: Change the autogenerated stub
        return $this->render('main_featured',[
            'post_model' => $this->posts_model,
            'post_count' => $this->count
        ]);
    }
}